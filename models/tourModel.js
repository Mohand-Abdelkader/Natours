const mongooes = require('mongoose');

const tourSchema = new mongooes.Schema({
  name: {
    type: String,
    required: [true, 'A tour must have a name'],
    unique: true,
  },
  rating: {
    type: Number,
    default: 4.5,
  },
  price: {
    type: Number,
    required: [true, 'A tour must have a price'],
  },
});
const Tour = mongooes.model('Tour', tourSchema);

module.exports = Tour;
